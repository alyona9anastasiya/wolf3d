#include "libft.h"

void	ft_putendl(char const *s)
{
	unsigned int i;

	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	if (s[i] == '\0')
		write(1, "\n", 1);
}
